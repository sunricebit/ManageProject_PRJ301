package Model;

/**
 *
 * @author ADMIN
 */
public class User {
    private String username, email, role, fullname, avatar;

    public User() {
    }

    public User(String username, String email, String role, String fullname, String avatar) {
        this.username = username;
        this.email = email;
        this.role = role;
        this.fullname = fullname;
        this.avatar = avatar;
    }

    
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

}
